// Helper: get the right icon component for a data source
export function SourceIcon({ source, size = 14, className }: { source: string; size?: number; className?: string }) {
  const color = SOURCE_COLORS[source] || "#666";
  const style = { color };
  switch (source) {
    case "github": return <span style={style}><IconGitHub size={size} className={className} /></span>;
    case "jira": return <span style={style}><IconJira size={size} className={className} /></span>;
    case "notion": return <span style={style}><IconNotion size={size} className={className} /></span>;
    case "ai": return <span style={style}><IconSparkle size={size} className={className} /></span>;
    case "session": return <span style={style}><IconBack size={size} className={className} /></span>;
    default: return null;
  }
}

// Helper: get color hex for a source
export function sourceColor(source: string): string {
  return SOURCE_COLORS[source] || "#666";
}
